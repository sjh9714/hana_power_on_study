#
# 구성:
#
# 1. Feature Engineering
# 2. KMeans 고객 세그먼트
# 3. Isolation Forest 이상 탐지
# 4. AutoEncoder 비지도 이상 탐지
# 5. XGBoost 지도학습
# 6. Rule Engine
#
# 목표:
#
# - 기존 사기 패턴 탐지
# - 새로운 이상 거래 탐지
# - 금융권 스타일 하이브리드 AI 구현
#
# ============================================================


# ============================================================
# 기본 라이브러리
# ============================================================

# 데이터 처리용
import pandas as pd

# 수학 계산용
import numpy as np

# 모델 저장/로드용
import joblib


# ============================================================
# 머신러닝 라이브러리
# ============================================================

# 데이터 정규화
from sklearn.preprocessing import StandardScaler

# 고객 세그먼트 생성
from sklearn.cluster import KMeans

# 이상 탐지 모델
from sklearn.ensemble import IsolationForest

# 학습/테스트 분리
from sklearn.model_selection import train_test_split

# 평가 지표
from sklearn.metrics import classification_report
from sklearn.metrics import roc_auc_score
from sklearn.metrics import precision_recall_curve
from sklearn.metrics import auc

# 지도학습 Fraud Detection 모델
from xgboost import XGBClassifier


# ============================================================
# 딥러닝(AutoEncoder)
# ============================================================

# 딥러닝 모델 생성용
from tensorflow.keras.models import Model

# 신경망 Layer
from tensorflow.keras.layers import Input, Dense

# 조기 종료(Early Stopping)
from tensorflow.keras.callbacks import EarlyStopping


# ============================================================
# 1. 데이터 로드
# ============================================================

# CSV 파일 로드
#
# 예:
# 거래 시간
# 카드 번호
# 거래 금액
# 가맹점 위치
# 사기 여부
#
df = pd.read_csv("data/credit_card_transactions.csv")


# ============================================================
# 2. 날짜 데이터 처리
# ============================================================

# 문자열 → datetime 타입 변환
#
# 이유:
# 시간 기반 Feature 생성 목적
#
df["trans_date_trans_time"] = pd.to_datetime(
    df["trans_date_trans_time"]
)

# ============================================================
# 거래 시간(hour)
# ============================================================

# 예:
# 2025-01-01 23:15:10
#
# → 23 추출
#
df["hour"] = df["trans_date_trans_time"].dt.hour

# ============================================================
# 요일(dayofweek)
# ============================================================

# 월=0 ~ 일=6
#
df["dayofweek"] = df["trans_date_trans_time"].dt.dayofweek

# ============================================================
# 월(month)
# ============================================================

df["month"] = df["trans_date_trans_time"].dt.month


# ============================================================
# 3. 야간 거래 여부
# ============================================================

# 금융권 핵심 Feature
#
# 일반적으로:
# 새벽 거래는 Fraud 비율 높음
#
# 조건:
# 22시 이후 OR 5시 이전
#
# 결과:
# 1 = 야간 거래
# 0 = 일반 거래
#
df["night_transaction"] = (
    (df["hour"] >= 22) |
    (df["hour"] <= 5)
).astype(int)


# ============================================================
# 4. 고객 위치 ↔ 가맹점 위치 거리 계산
# ============================================================

# 고객 위치:
# lat, long
#
# 가맹점 위치:
# merch_lat, merch_long
#
# 금융권 핵심:
#
# 비정상 이동 거리 탐지
#
# 예:
# 서울 → 부산 → 미국
# 5분 내 이동
#
# => Fraud 가능성 높음
#
df["distance"] = np.sqrt(

    # 위도 차이 제곱
    (df["lat"] - df["merch_lat"]) ** 2 +

    # 경도 차이 제곱
    (df["long"] - df["merch_long"]) ** 2
)


# ============================================================
# 5. 고객 평균 소비 대비 현재 거래 비율
# ============================================================

# 고객 평균 거래 금액 계산
#
customer_avg_amt = df.groupby("cc_num")["amt"].transform("mean")

# ============================================================
# amount_ratio 생성
# ============================================================

# 의미:
#
# 현재 거래가
# 고객 평소 소비보다 얼마나 큰가?
#
# 예:
#
# 평소 평균 = 50달러
# 현재 거래 = 5000달러
#
# => 매우 위험
#
df["amount_ratio"] = (
    df["amt"] / (customer_avg_amt + 1)
)


# ============================================================
# 6. 고객 거래 횟수
# ============================================================

# 고객별 총 거래 횟수 계산
#
# 이유:
#
# 거래 빈도 패턴 분석
#
df["customer_tx_count"] = df.groupby("cc_num")[
    "amt"
].transform("count")


# ============================================================
# 7. 이전 거래와 시간 차이
# ============================================================

# 카드번호 + 시간 기준 정렬
#
df = df.sort_values(
    ["cc_num", "trans_date_trans_time"]
)

# ============================================================
# 이전 거래 시간 추출
# ============================================================

# shift(1):
#
# 바로 이전 거래
#
df["prev_trans_time"] = df.groupby("cc_num")[
    "trans_date_trans_time"
].shift(1)

# ============================================================
# 현재 거래 - 이전 거래
# ============================================================

# 단위:
# 초(seconds)
#
df["time_diff"] = (
    df["trans_date_trans_time"] -
    df["prev_trans_time"]
).dt.total_seconds()

# 첫 거래는 NaN 발생
#
# 매우 큰 값으로 처리
#
df["time_diff"] = df["time_diff"].fillna(999999)


# ============================================================
# 8. Velocity Feature
# ============================================================

# 매우 짧은 시간 안에
# 연속 거래 발생 여부
#
# 금융권 핵심 Feature
#
# 예:
#
# 10초 내 반복 결제
#
# => Fraud 가능성 매우 높음
#
df["velocity_flag"] = (
    df["time_diff"] < 60
).astype(int)


# ============================================================
# 9. 머신러닝 Feature 목록
# ============================================================

features = [

    # 거래 금액
    "amt",

    # 거래 시간
    "hour",

    # 요일
    "dayofweek",

    # 월
    "month",

    # 야간 거래 여부
    "night_transaction",

    # 이동 거리
    "distance",

    # 평균 대비 소비 비율
    "amount_ratio",

    # 고객 거래 횟수
    "customer_tx_count",

    # 이전 거래와 시간 차이
    "time_diff",

    # 짧은 시간 내 반복 거래 여부
    "velocity_flag"

]

# ============================================================
# 입력 데이터(X)
# ============================================================

X = df[features]

# ============================================================
# 정답(Label)
# ============================================================

# 0 = 정상
# 1 = Fraud
#
y = df["is_fraud"]


# ============================================================
# 10. 데이터 정규화
# ============================================================

# 머신러닝은 Feature 크기에 민감
#
# 예:
#
# amt = 5000
# velocity_flag = 0 or 1
#
# => 금액이 너무 큼
#
# 따라서:
#
# 평균=0
# 표준편차=1
#
# 형태로 정규화 수행
#
scaler = StandardScaler()

X_scaled = scaler.fit_transform(X)


# ============================================================
# 11. KMeans 고객 세그먼트
# ============================================================

# 금융권 핵심 아이디어:
#
# 모든 고객을 동일 기준으로 판단 X
#
# 유사 고객 그룹 내부 비교 O
#
# 예:
#
# VIP
# 학생
# 야간 소비형
# 해외 사용자
#
# 등을 자동 그룹화
#
kmeans = KMeans(
    n_clusters=6,
    random_state=42
)

# ============================================================
# 고객 세그먼트 생성
# ============================================================

df["segment"] = kmeans.fit_predict(X_scaled)


# ============================================================
# 12. Isolation Forest
# ============================================================

# 비지도 이상 탐지 모델
#
# 핵심:
#
# 정상 데이터와
# 다른 패턴 탐지
#
iso = IsolationForest(

    # 트리 개수
    n_estimators=300,

    # 이상 데이터 비율 가정
    contamination=0.01,

    random_state=42
)

# ============================================================
# 학습
# ============================================================

iso.fit(X_scaled)

# ============================================================
# 이상 점수 생성
# ============================================================

# 점수가 낮을수록 이상
#
df["iso_score"] = iso.decision_function(X_scaled)


# ============================================================
# 13. AutoEncoder
# ============================================================

# 정상 거래만 사용
#
# 이유:
#
# 정상 패턴 학습 후
#
# 복원 실패하면 이상 거래로 판단
#
X_normal = X_scaled[y == 0]


# ============================================================
# 입력 차원 수
# ============================================================

input_dim = X_scaled.shape[1]


# ============================================================
# 입력 Layer
# ============================================================

input_layer = Input(shape=(input_dim,))


# ============================================================
# Encoder
# ============================================================

# 차원 압축
#
# 정상 패턴 특징만 추출
#
encoder = Dense(32, activation="relu")(input_layer)

encoder = Dense(16, activation="relu")(encoder)

encoder = Dense(8, activation="relu")(encoder)


# ============================================================
# Decoder
# ============================================================

# 다시 원래 데이터로 복원
#
decoder = Dense(16, activation="relu")(encoder)

decoder = Dense(32, activation="relu")(decoder)

decoder = Dense(input_dim, activation="linear")(decoder)


# ============================================================
# AutoEncoder 모델 생성
# ============================================================

autoencoder = Model(
    inputs=input_layer,
    outputs=decoder
)


# ============================================================
# 모델 컴파일
# ============================================================

# loss="mse"
#
# 복원 오차 최소화
#
autoencoder.compile(
    optimizer="adam",
    loss="mse"
)


# ============================================================
# 조기 종료
# ============================================================

# 과적합 방지
#
early_stop = EarlyStopping(

    monitor="val_loss",

    patience=5,

    restore_best_weights=True
)


# ============================================================
# AutoEncoder 학습
# ============================================================

autoencoder.fit(

    # 입력
    X_normal,

    # 출력도 동일
    X_normal,

    epochs=50,

    batch_size=256,

    validation_split=0.2,

    callbacks=[early_stop],

    verbose=1
)


# ============================================================
# 14. Reconstruction Error 계산
# ============================================================

# AutoEncoder 복원 결과
#
reconstructed = autoencoder.predict(X_scaled)

# ============================================================
# MSE 계산
# ============================================================

# 복원 오차 계산
#
# 정상 거래:
# 오차 작음
#
# 이상 거래:
# 오차 큼
#
mse = np.mean(
    np.power(X_scaled - reconstructed, 2),
    axis=1
)

# ============================================================
# Reconstruction Error 저장
# ============================================================

df["reconstruction_error"] = mse


# ============================================================
# 15. 최종 XGBoost 입력
# ============================================================

final_features = [

    "amt",
    "hour",
    "dayofweek",
    "month",
    "night_transaction",
    "distance",
    "amount_ratio",
    "customer_tx_count",
    "time_diff",
    "velocity_flag",

    # KMeans 결과
    "segment",

    # Isolation Forest 결과
    "iso_score",

    # AutoEncoder 결과
    "reconstruction_error"

]

X_final = df[final_features]


# ============================================================
# 16. Train/Test 분리
# ============================================================

X_train, X_test, y_train, y_test = train_test_split(

    X_final,
    y,

    # 테스트 20%
    test_size=0.2,

    # 클래스 비율 유지
    stratify=y,

    random_state=42
)


# ============================================================
# 17. 클래스 불균형 처리
# ============================================================

# Fraud 데이터는 매우 적음
#
# 따라서 Fraud 가중치 증가 필요
#
scale_pos_weight = (
    y.value_counts()[0] /
    y.value_counts()[1]
)


# ============================================================
# 18. XGBoost 모델 생성
# ============================================================

model = XGBClassifier(

    # 트리 개수
    n_estimators=500,

    # 트리 깊이
    max_depth=7,

    # 학습 속도
    learning_rate=0.03,

    # 랜덤 샘플링
    subsample=0.8,

    # Feature 샘플링
    colsample_bytree=0.8,

    # 클래스 불균형 보정
    scale_pos_weight=scale_pos_weight,

    # 평가 지표
    eval_metric="aucpr",

    random_state=42
)


# ============================================================
# 19. XGBoost 학습
# ============================================================

model.fit(X_train, y_train)


# ============================================================
# 20. 예측
# ============================================================

pred = model.predict(X_test)

# Fraud 확률
#
proba = model.predict_proba(X_test)[:, 1]


# ============================================================
# 21. 성능 평가
# ============================================================

print("=" * 50)
print("Classification Report")
print("=" * 50)

print(classification_report(y_test, pred))


# ============================================================
# ROC-AUC
# ============================================================

roc_auc = roc_auc_score(y_test, proba)

print("ROC-AUC:", roc_auc)


# ============================================================
# PR-AUC
# ============================================================

# 금융권에서 매우 중요
#
precision, recall, _ = precision_recall_curve(
    y_test,
    proba
)

pr_auc = auc(recall, precision)

print("PR-AUC:", pr_auc)


# ============================================================
# 22. Rule Engine
# ============================================================

# 실제 금융권은:
#
# AI만 사용하지 않음
#
# Rule 기반 정책 함께 사용
#
df_test = X_test.copy()

df_test["fraud_probability"] = proba

# ============================================================
# Rule 점수 초기화
# ============================================================

df_test["rule_score"] = 0


# ============================================================
# 고액 거래
# ============================================================

df_test.loc[
    df_test["amt"] > 3000,
    "rule_score"
] += 1


# ============================================================
# 야간 거래
# ============================================================

df_test.loc[
    df_test["night_transaction"] == 1,
    "rule_score"
] += 1


# ============================================================
# 매우 짧은 거래 간격
# ============================================================

df_test.loc[
    df_test["time_diff"] < 30,
    "rule_score"
] += 1


# ============================================================
# 이동 거리 큼
# ============================================================

df_test.loc[
    df_test["distance"] > 5,
    "rule_score"
] += 1


# ============================================================
# 23. 최종 위험 점수
# ============================================================

# AI 결과 + Rule 결과 결합
#
df_test["final_score"] = (

    # AI 점수
    df_test["fraud_probability"] * 0.7 +

    # Rule 점수
    (df_test["rule_score"] / 4) * 0.3
)


# ============================================================
# 24. 모델 저장
# ============================================================

# XGBoost 저장
joblib.dump(model, "app/model/xgb_model.pkl")

# KMeans 저장
joblib.dump(kmeans, "app/model/kmeans.pkl")

# Isolation Forest 저장
joblib.dump(iso, "app/model/isolation_forest.pkl")

# Scaler 저장
joblib.dump(scaler, "app/model/scaler.pkl")

# AutoEncoder 저장
autoencoder.save("app/model/autoencoder.h5")


# ============================================================
# 완료 메시지
# ============================================================

print("모든 모델 저장 완료")