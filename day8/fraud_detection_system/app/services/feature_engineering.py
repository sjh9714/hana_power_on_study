import numpy as np

# ============================================================
# 실시간 Feature 생성 함수
# ============================================================

# 입력: tx
#     현재 거래 정보
#
# customer_state
#     Redis에 저장된 고객 상태

# 출력: feature
#     AI 모델(XGBoost) 입력값
#
# updated_state
#     Redis에 다시 저장할 고객 최신 상태
#
def create_features(tx, customer_state):

    # ========================================================
    # 1. 고객 위치 ↔ 가맹점 위치 거리 계산
    # ========================================================

    # 현재 거래 위치와
    # 가맹점 위치 거리 계산
    #
    # 실제 금융권:
    #
    # 갑자기 매우 먼 거리 이동
    #
    # 예:
    #
    # 서울
    # ↓ 5분
    # 부산
    #
    # → 불가능한 이동
    #
    # → 위험 증가
    #
    # 현재 예제:
    #
    # 단순 유클리드 거리
    #
    # 실제 금융권:
    #
    # Haversine 공식(GPS 거리)
    # 사용

    distance = np.sqrt(

        # 위도 차이 제곱
        (tx.lat - tx.merch_lat) ** 2 +

        # 경도 차이 제곱
        (tx.long - tx.merch_long) ** 2
    )

    # ========================================================
    # 2. 거래 횟수 증가
    # ========================================================

    # Redis에 저장된
    # 이전 거래 횟수 +1
    #
    # 이유:
    #
    # 현재 거래 발생했기 때문
    #
    # 예:
    #
    # 기존:
    #
    # transaction_count=10
    #
    # 현재 거래 발생
    #
    # →11

    transaction_count = (
        customer_state["transaction_count"] + 1
    )

    # ========================================================
    # 3. 누적 거래 금액 계산
    # ========================================================

    # 이전 거래 총합
    #
    # +
    #
    # 현재 거래 금액

    total_amount = (customer_state["total_amount"] + tx.amt)

    # ========================================================
    # 4. 평균 거래 금액 계산
    # ========================================================

    # 금융권 핵심 Feature
    #
    # 고객 평소 소비 패턴 생성
    #
    # 예:
    #
    # 평균:
    #
    # 5만원
    #
    # 갑자기:
    #
    # 500만원
    #
    # → 위험 가능성

    avg_amount = (total_amount / transaction_count)

    # ========================================================
    # 5. 야간 거래 수 조회
    # ========================================================

    # Redis 이전 상태 읽기

    night_count = customer_state["night_count"]

    # ========================================================
    # 6. 현재 거래가 야간인지 확인
    # ========================================================

    # 금융권 핵심 Feature
    #
    # 새벽 거래는
    # 사기 비율이 높아지는 경향 존재
    #
    # 조건:
    #
    # 22시 이후
    #
    # 또는
    #
    # 5시 이전

    if tx.hour >= 22 or tx.hour <= 5:

        # 야간 거래 횟수 증가
        night_count += 1

    # ========================================================
    # 7. 야간 거래 비율 계산
    # ========================================================

    # night_ratio:
    #
    # 전체 거래 중
    # 야간 거래 비율
    #
    # 예:
    #
    # 거래:
    #
    # 20건
    #
    # 야간:
    #
    # 5건
    #
    # 결과:
    #
    # 5/20
    #
    # =0.25

    night_ratio = (night_count / transaction_count)

    # ========================================================
    # 8. 누적 이동 거리 계산
    # ========================================================

    # 이전 누적 거리
    #
    # +
    #
    # 현재 거리

    total_distance = (customer_state["total_distance"] + distance)

    # ========================================================
    # 9. 평균 이동 거리 계산
    # ========================================================

    # 고객 평소 이동 패턴
    #
    # 예:
    #
    # 평소:
    #
    # 2km
    #
    # 갑자기:
    #
    # 300km
    #
    # 위험 증가

    avg_distance = (total_distance / transaction_count)

    # ========================================================
    # 10. AI 입력 Feature 생성
    # ========================================================

    # XGBoost 모델 입력값
    #
    # 실시간 예측용

    features = {

        # 거래 횟수
        #
        # Velocity 탐지
        #
        # 짧은 시간 내 급증 여부
        "transaction_count": transaction_count,

        # 평균 소비 금액
        #
        # 고객 패턴
        "avg_amount": avg_amount,

        # 소비 변동성
        #
        # 현재 예제:
        #
        # 계산 생략
        #
        # 실제:
        #
        # 표준편차 사용
        "std_amount": 0,

        # 야간 거래 비율
        "night_ratio": night_ratio,

        # 평균 이동 거리
        "avg_distance": avg_distance

    }

    # ========================================================
    # 11. Redis 저장용 최신 상태 생성
    # ========================================================

    # 거래 완료 후
    #
    # Redis에 다시 저장할 데이터

    updated_state = {

        # 최신 거래 횟수
        "transaction_count": transaction_count,

        # 최신 누적 금액
        "total_amount": total_amount,

        # 최신 야간 거래 수
        "night_count": night_count,

        # 최신 누적 거리
        "total_distance": total_distance

    }

    # ========================================================
    # 반환
    # ========================================================

    return (
        # AI 입력값
        features,

        # Redis 저장 데이터
        updated_state
    )

