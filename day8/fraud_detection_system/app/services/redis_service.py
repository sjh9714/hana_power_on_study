# Redis:
#
# 메모리 기반 초고속 Key-Value 저장소
#
# 금융권에서는:
# - 실시간 고객 상태 저장
# - Session 관리
# - Velocity 탐지
# - Feature Store
#
# 등에 매우 많이 사용됨

import redis
import os

# ============================================================
# JSON 라이브러리 import
# ============================================================

# Redis는 문자열(String) 기반 저장
#
# Python Dictionary 객체를
# 문자열 형태(JSON)로 변환하기 위해 사용

import json

# ============================================================
# Redis 서버 연결
# ============================================================

# Redis 서버 접속 객체 생성
#
# host:
# Redis 서버 주소
#
# port:
# Redis 기본 포트 = 6379
#
# decode_responses=True:
#
# Redis 응답값을 bytes가 아니라
# 문자열(str)로 자동 변환
#
# 예:
#
# b'hello'
# → 'hello'
redis_client = redis.Redis(
    host=os.getenv(
        "REDIS_HOST",
        "localhost"
    ),

    port=int(
        os.getenv(
            "REDIS_PORT",
            6379
        )
    ),
    decode_responses=True
)

# ============================================================
# 고객 상태 조회 함수
# ============================================================

# 역할:
#
# 현재 고객의 실시간 상태 조회
#
# 예:
#
# 최근 거래 횟수
# 최근 거래 금액 합계
# 최근 야간 거래 수
#
# 등을 Redis에서 읽음
#
# 금융권 핵심 개념:
#
# Stateful Fraud Detection
#
# 즉:
#
# 현재 거래만 보는 것이 아니라
# "이전 거래 상태"
# 를 함께 분석

def get_customer_state(cc_num):

    # ========================================================
    # Redis Key 조회
    # ========================================================

    # 카드번호(cc_num)를 Key로 사용
    #
    # str(cc_num):
    #
    # Redis Key는 문자열 형태 사용
    #
    # 예:
    #
    # "123456789"
    #
    # Redis 내부:
    #
    # Key   = "123456789"
    # Value = "{...json...}"

    data = redis_client.get(str(cc_num))

    # ========================================================
    # Redis 데이터 존재 여부 확인
    # ========================================================

    # 고객 상태가 이미 Redis에 존재하면
    #
    # 예:
    #
    # {
    #   "transaction_count": 10,
    #   "total_amount": 5000
    # }

    if data:

        # ====================================================
        # JSON 문자열 → Python Dictionary 변환
        # ====================================================

        # Redis에는 문자열(JSON)로 저장됨
        #
        # 따라서 Python 객체로 다시 변환 필요
        #
        # 예:
        #
        # '{"count":10}'

        return json.loads(data)

    # ========================================================
    # Redis에 데이터가 없는 경우
    # ========================================================

    # 의미:
    #
    # - 신규 고객
    # - TTL 만료 고객
    # - 아직 거래 없는 고객
    #
    # 금융권에서는:
    #
    # Cold Start 문제
    #
    # 라고도 부름
    #
    # 현재 예제에서는
    # 기본 초기값 생성

    return {

        # ====================================================
        # 총 거래 횟수
        # ====================================================

        # 고객이 현재까지 거래한 횟수
        #
        # Velocity 탐지에 사용
        #
        # 예:
        #
        # 1분 내 20건 결제
        #
        # → 이상 가능성

        "transaction_count": 0,

        # ====================================================
        # 총 거래 금액
        # ====================================================

        # 고객 누적 거래 금액
        #
        # 평균 소비 금액 계산용
        #
        # avg_amount =
        # total_amount / transaction_count

        "total_amount": 0,

        # ====================================================
        # 야간 거래 횟수
        # ====================================================

        # 야간 거래 비율 계산용
        #
        # night_ratio =
        # night_count / transaction_count
        #
        # 금융권 핵심 Feature

        "night_count": 0,

        # ====================================================
        # 총 이동 거리
        # ====================================================

        # 고객 위치 ↔ 가맹점 위치
        #
        # 누적 이동 거리
        #
        # 평균 이동 거리 계산용
        #
        # avg_distance =
        # total_distance / transaction_count

        "total_distance": 0

    }

# ============================================================
# 고객 상태 저장 함수
# ============================================================

# 역할:
#
# 최신 고객 상태를 Redis에 저장
#
# 실시간 거래가 발생할 때마다:
#
# 거래 횟수 증가
# 평균 금액 변경
# 야간 거래 증가
#
# 등의 상태 업데이트 수행

def save_customer_state(cc_num, state):

    # ========================================================
    # Redis 저장
    # ========================================================

    # Key:
    # 카드번호(cc_num)
    #
    # Value:
    # 고객 상태(JSON)
    #
    # 예:
    #
    # Key:
    # "123456"
    #
    # Value:
    #
    # {
    #   "transaction_count": 15,
    #   "total_amount": 8500,
    #   "night_count": 3,
    #   "total_distance": 120
    # }

    redis_client.set(

        # Redis Key
        str(cc_num),

        # Dictionary → JSON 문자열 변환
        json.dumps(state)
    )
