# ============================================================
# Risk Score 계산 함수
# ============================================================

# 입력:
#
# probability
#
# XGBoost 모델이 예측한 사기(Fraud) 확률
#
#
# 예:
#
# 0.03
# → 사기 가능성 3%
#
# 0.85
# → 사기 가능성 85%
#
#
# 출력:
#
# score
#     위험 점수 (0~100)
#
# action
#     실제 업무 처리 결과
#
#         APPROVE
#         MFA
#         BLOCK
#
#
# 금융권 핵심:
#
# AI 결과를 그대로 사용하지 않음
#
# 반드시:
#
# 업무 정책(Rule)
#
# 으로 변환

def calculate_risk(probability):

    # ========================================================
    # 1. 확률 → Risk Score 변환
    # ========================================================

    # XGBoost는:
    #
    # 0~1 범위 확률 출력
    #
    # 예: 0.92
    #
    # 사람이 보기 어렵기 때문에
    #
    # 0~100 점수로 변환
    #
    # 예:
    #
    # 0.92 
    #
    # ↓
    #
    # 92점

    score = probability * 100

    # ========================================================
    # 2. 매우 위험
    # ========================================================

    # 80점 이상
    #
    # 의미:
    #
    # 거의 사기로 판단
    #
    # 실제 금융권:
    #
    # 즉시 차단
    #
    # 또는
    #
    # 승인 보류

    if score >= 80:

        action = "BLOCK"

    # ========================================================
    # 3. 중간 위험
    # ========================================================

    # 60~80
    #
    # 완전 사기라고 단정 어려움
    #
    # 예:
    #
    # 해외여행 중
    # 새 기기 로그인
    # 고액 결제
    #
    # 실제 사용자일 수도 있음
    #
    # 금융권:
    #
    # MFA(추가 인증) 수행

    elif score >= 60:

        action = "MFA"

    # ========================================================
    # 4. 낮은 위험
    # ========================================================

    # 정상 거래 가능성 높음
    #
    # 즉시 승인

    else:

        action = "APPROVE"

    # ========================================================
    # 5. 결과 반환
    # ========================================================

    return score, action

