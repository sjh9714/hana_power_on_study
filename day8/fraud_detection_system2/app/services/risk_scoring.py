# ============================================================
# Risk Score 계산
# ============================================================

def calculate_risk(final_score):

    # ========================================================
    # Risk Score
    # ========================================================

    risk_score = final_score * 100

    # ========================================================
    # 금융권 정책
    # ========================================================

    # 0 ~ 40
    #
    # 정상 승인
    #
    if risk_score < 40:
        action = "APPROVE"

    # ========================================================
    # 40 ~ 70
    # ========================================================

    # 추가 인증 요구
    #
    # MFA:
    # SMS
    # OTP
    # FIDO
    #
    elif risk_score < 70:
        action = "MFA"

    # ========================================================
    # 70 이상
    # ========================================================

    # 거래 차단
    #
    else:
        action = "BLOCK"

    return risk_score, action