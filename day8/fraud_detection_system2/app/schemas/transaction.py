# FastAPI 요청 검증용
from pydantic import BaseModel

# 거래 시간 처리
from datetime import datetime


# ============================================================
# 거래 요청 객체
# ============================================================

class Transaction(BaseModel):

    # ========================================================
    # 카드 번호
    # ========================================================

    cc_num: int

    # ========================================================
    # 거래 금액
    # ========================================================

    amt: float

    # ========================================================
    # 거래 시간
    # ========================================================

    trans_date_trans_time: datetime

    # ========================================================
    # 고객 위치
    # ========================================================

    lat: float
    long: float

    # ========================================================
    # 가맹점 위치
    # ========================================================

    merch_lat: float
    merch_long: float