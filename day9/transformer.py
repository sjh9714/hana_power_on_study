import numpy as np

# =====================================================
# 1. 입력 문장 벡터 (Embedding)
# =====================================================
# 문장:
# "나는 피자를 먹었다"
#
# 실제 GPT에서는 수천 차원의 임베딩 벡터가 사용되지만
# 학습용으로 3차원 벡터를 사용

X = np.array([
    [1, 0, 1],   # 나는
    [0, 2, 1],   # 피자를
    [1, 1, 2]    # 먹었다
])

print("=== 입력 임베딩 ===")
print(X)

# =====================================================
# 2. Query 가중치 행렬 생성
# =====================================================
# 실제 Transformer에서는 학습을 통해 최적화되는 파라미터
#
# Wq : Query 생성용 가중치
# Wk : Key 생성용 가중치
# Wv : Value 생성용 가중치
#
# 현재는 이해를 위해 랜덤값 사용

Wq = np.random.rand(3, 3)
Wk = np.random.rand(3, 3)
Wv = np.random.rand(3, 3)

print("\n=== Wq ===")
print(Wq)

print("\n=== Wk ===")
print(Wk)

print("\n=== Wv ===")
print(Wv)

# =====================================================
# 3. Query 생성
# =====================================================
# 입력 벡터 X와 Wq를 곱하여 Query 생성
#
# Query의 의미:
# "나는 지금 어떤 정보를 찾고 싶은가?"
#
# 예:
# "먹었다"는
# "누가 먹었지?"
# "무엇을 먹었지?"
# 와 같은 정보를 찾으려고 함

Q = X @ Wq

# =====================================================
# 4. Key 생성
# =====================================================
# 입력 벡터 X와 Wk를 곱하여 Key 생성
#
# Key의 의미:
# "내가 가지고 있는 정보는 무엇인가?"
#
# Query가 질문이라면
# Key는 답변 후보

K = X @ Wk

# =====================================================
# 5. Value 생성
# =====================================================
# 입력 벡터 X와 Wv를 곱하여 Value 생성
#
# Value의 의미:
# 실제 전달할 정보
#
# Query와 Key를 비교하여
# 중요도가 높으면 해당 Value를 많이 가져옴

V = X @ Wv

# =====================================================
# 결과 출력
# =====================================================

print("\n============================")
print("Q (Query)")
print("============================")
print(Q)

print("\n============================")
print("K (Key)")
print("============================")
print(K)

print("\n============================")
print("V (Value)")
print("============================")
print(V)